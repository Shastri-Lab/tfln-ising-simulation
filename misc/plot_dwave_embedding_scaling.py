import dimod
import numpy as np
import networkx as nx
import dwave_networkx as dnx
import matplotlib.pyplot as plt
from minorminer import busclique
from minorminer import find_embedding
# from dwave.embedding.pegasus import find_clique_embedding

sizes = np.arange(4, 128, 2)

# make a fully connected graph of all sizes
k_graphs = [nx.complete_graph(size) for size in sizes]

# big enough pegasus graph for all sizes
C = dnx.pegasus_graph(12)

# get the embeddings of each graph
embeddings = [busclique.find_clique_embedding(size, C) for size in sizes]

# get the number of qubits required for each embedding
qubits = [sum([len(k) for k in embedding.values()]) for embedding in embeddings]

# fit with a quadratic
coeffs = np.polyfit(sizes, qubits, 2)
qubits_fit = np.polyval(coeffs, sizes)

# plot the results
plt.figure(figsize=(6, 4))
plt.plot(sizes, qubits, '.', markersize=10, color='black')
# fit label like f'$N_p=${coeffs[0]:.2f}$N^2$ + {coeffs[1]:.2f}$N$ + {coeffs[2]:.2f}' but make sure + or - is correct
fit_label = 'Pegasus: '
for i, coeff in enumerate(coeffs):
    if i == 0:
        fit_label += f'{coeff:.2f}$N^2$'
    elif i == 1:
        if coeff < 0:
            fit_label += f' - {abs(coeff):.2f}$N$'
        else:
            fit_label += f' + {coeff:.2f}$N$'
    elif i == 2:
        if coeff < 0:
            fit_label += f' - {abs(coeff):.2f}'
        else:
            fit_label += f' + {coeff:.2f}'
plt.plot(sizes, qubits_fit, '-', label=fit_label, color='red')
plt.plot(sizes, sizes, '--', label='CEOM', color='blue')
plt.xlabel('Complete Graph Size')
plt.ylabel('Required Physical Qubits')
plt.title('Pegasus Topology Embedding Scaling')
plt.legend()
plt.show()



